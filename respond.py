def get_playbook(attack_type: str) -> list:
    return PLAYBOOKS.get(attack_type, PLAYBOOKS["default"])


# ── Sigma Rule Generator ─────────────────────────────────────────────────────
def generate_sigma_rules(findings: list) -> str:
    anomalous = [f for f in findings if f.get("is_anomalous")]
    if not anomalous:
        return "# No anomalous findings — no Sigma rules generated.\n"

    seen = set()
    rules = []
    for f in anomalous:
        title = f.get("sigma_title") or f.get("attack_type","Unknown")
        if title in seen:
            continue
        seen.add(title)
        techniques = f.get("mitre_techniques", [])
        mitre_block = "\n".join(f"        - {t}" for t in techniques) if techniques else "        - T0000"
        rule = f"""---
title: {title}
id: threatsight-{hash(title) & 0xFFFFFF:06x}
status: experimental
description: >
    Detects {f.get('attack_type','unknown attack')} based on ThreatSight analysis.
    Generated by ThreatSight v2.0 | Author: Krutika Jagdale
references:
    - https://attack.mitre.org/techniques/{techniques[0].replace('.','/')+'/' if techniques else ''}
author: Krutika Jagdale
date: {datetime.now(timezone.utc).strftime('%Y/%m/%d')}
tags:
    - attack.{f.get('category','security').lower().replace(' ','_')}
{mitre_block}
logsource:
    product: windows
    service: security
detection:
    selection:
        query|contains:
            - '{f.get('attack_type','').lower()}'
    condition: selection
falsepositives:
    - Legitimate administrative activity
    - Security testing and pen testing
level: {f.get('risk_level','medium')}
"""
        rules.append(rule)
    return "\n".join(rules)


