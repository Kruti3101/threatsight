# ── Plain-text report ────────────────────────────────────────────────────────
def generate_text_report(findings, correlation, stats, sigma_rules, output_path):
    now = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M:%S UTC")
    anomalous = [f for f in findings if f.get("is_anomalous")]
    lines = [
        "=" * 72,
        "  ThreatSight v2.0 — Security Detection Report",
        f"  Generated : {now}",
        f"  Author    : Krutika Jagdale",
        "=" * 72,
        "",
        "EXECUTIVE SUMMARY",
        "-" * 40,
        f"  Events analyzed   : {stats['total']}",
        f"  Threats detected  : {stats['anomalous']}",
        f"  Clean events      : {stats['clean']}",
        f"  Kill chain steps  : {len(correlation.get('kill_chain',[]))}",
        "",
    ]
    campaign = correlation.get("campaign")
    if campaign and campaign.get("detected"):
        lines += [
            f"  ⚠ CAMPAIGN DETECTED ({campaign.get('confidence','').upper()} confidence)",
            f"  {campaign.get('description','')}",
            f"  ACTION: {campaign.get('recommendation','')}",
            "",
        ]
    lines += ["RISK BREAKDOWN", "-" * 40]
    for lvl in ["critical","high","medium","low","none"]:
        count = stats.get("by_risk",{}).get(lvl,0)
        if count:
            lines.append(f"  {lvl.upper():<12}: {count}")
    lines += ["", "KILL CHAIN", "-" * 40]
    for step in correlation.get("kill_chain",[]):
        lines.append(f"  Step {step['step']:02d} | {step['tactic']:<25} | {step['attack_type']}")
    lines += ["", "THREAT FINDINGS", "-" * 40]
    for f in anomalous:
        lines += [
            f"\n  [{f.get('risk_level','?').upper()}] {f.get('attack_type','')} — Event {f.get('query_id','')}",
            f"  Time      : {f.get('timestamp','')}",
            f"  User      : {f.get('user','')} ({f.get('source_ip','')})",
            f"  Category  : {f.get('category','')}",
            f"  MITRE     : {', '.join(f.get('mitre_techniques',[]))}",
            f"  Reasoning : {f.get('reasoning','')}",
            f"  Rule      : {f.get('detection_rule','')}",
            "  Playbook  :",
        ]
        for step in get_playbook(f.get("attack_type","")):
            lines.append(f"    {step}")
    lines += ["", "", "SIGMA DETECTION RULES", "-" * 40, "", sigma_rules, "", "=" * 72]
    Path(output_path).parent.mkdir(parents=True, exist_ok=True)
    Path(output_path).write_text("\n".join(lines), encoding="utf-8")
    return output_path


# ── JSON export for SIEM ─────────────────────────────────────────────────────
def generate_json_export(findings, correlation, stats, output_path):
    payload = {
        "meta": {
            "generated": datetime.now(timezone.utc).isoformat(),
            "tool": "ThreatSight v2.0",
            "author": "Krutika Jagdale",
        },
        "stats": stats,
        "correlation": correlation,
        "findings": findings,
    }
    Path(output_path).parent.mkdir(parents=True, exist_ok=True)
    Path(output_path).write_text(json.dumps(payload, indent=2), encoding="utf-8")
    return output_path
